//! Owned TOML reader for `tools/frame-pacing/budgets.toml`.
//!
//! Mirrors the pattern in `engine_script::breakpoints_toml` — line-by-line
//! parse, `key = value` after a `[budgets]` section header. No serde, no
//! third-party TOML parser. The schema is fixed at two scalar fields; any
//! line outside the schema is ignored so future ADR-amend additions read
//! cleanly on old binaries.

use std::path::Path;

/// Frame-pacing thresholds. Both fields default to `None` when the file is
/// silent on them; the caller falls back to the spec-published defaults
/// (ADR-047 §3: 18.3 ms / 1.04 ms).
#[derive(Clone, Copy, Debug, Default, PartialEq)]
pub struct Budgets {
    /// p99 frame-time budget (milliseconds).
    pub p99_ms: Option<f64>,
    /// σ frame-time budget (milliseconds).
    pub stddev_ms: Option<f64>,
}

/// Load + parse a budgets file from disk.
pub fn read_from_path(path: &Path) -> Result<Budgets, String> {
    let body = std::fs::read_to_string(path).map_err(|e| format!("read {path:?}: {e}"))?;
    Ok(parse(&body))
}

/// Parse a budgets-TOML body. Tolerates blank lines, `#` comments, and
/// arbitrary whitespace. Section headers other than `[budgets]` are
/// ignored.
pub fn parse(source: &str) -> Budgets {
    let mut budgets = Budgets::default();
    let mut in_budgets = false;
    for raw in source.lines() {
        let line = strip_comment(raw).trim();
        if line.is_empty() {
            continue;
        }
        if line.starts_with('[') && line.ends_with(']') {
            in_budgets = line == "[budgets]";
            continue;
        }
        if !in_budgets {
            continue;
        }
        let Some((key, value)) = line.split_once('=') else {
            continue;
        };
        let key = key.trim();
        let value = strip_comment(value).trim();
        match key {
            "p99_ms" => {
                if let Ok(v) = value.parse::<f64>() {
                    budgets.p99_ms = Some(v);
                }
            }
            "stddev_ms" => {
                if let Ok(v) = value.parse::<f64>() {
                    budgets.stddev_ms = Some(v);
                }
            }
            _ => {}
        }
    }
    budgets
}

/// Strip a trailing `# comment` from a line. Treats the first un-escaped
/// `#` as the comment start; this file's strings never embed `#`, so no
/// escape handling is required.
fn strip_comment(line: &str) -> &str {
    match line.find('#') {
        Some(i) => &line[..i],
        None => line,
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn parse_canonical_budgets_returns_both_fields() {
        let src = "\
[budgets]
p99_ms = 18.3
stddev_ms = 1.04
";
        let b = parse(src);
        assert_eq!(b.p99_ms, Some(18.3));
        assert_eq!(b.stddev_ms, Some(1.04));
    }

    #[test]
    fn parse_tolerates_comments_and_blank_lines() {
        let src = "\
# Frame-pacing budgets
# Generated by hand.

[budgets]
p99_ms     = 18.3   # ADR-047 §3
stddev_ms  = 1.04   # tight; needs sched-ext.
";
        let b = parse(src);
        assert_eq!(b.p99_ms, Some(18.3));
        assert_eq!(b.stddev_ms, Some(1.04));
    }

    #[test]
    fn parse_returns_none_when_section_missing() {
        let src = "p99_ms = 18.3\nstddev_ms = 1.04\n";
        let b = parse(src);
        assert_eq!(b.p99_ms, None);
        assert_eq!(b.stddev_ms, None);
    }

    #[test]
    fn parse_ignores_unrelated_sections() {
        let src = "[other]\np99_ms = 99.9\n";
        let b = parse(src);
        assert_eq!(b.p99_ms, None);
    }

    #[test]
    fn parse_handles_missing_fields_independently() {
        let src = "[budgets]\np99_ms = 12.5\n";
        let b = parse(src);
        assert_eq!(b.p99_ms, Some(12.5));
        assert_eq!(b.stddev_ms, None);
    }

    #[test]
    fn parse_rejects_garbage_values() {
        // Bad value strings leave the field at `None`; the caller falls
        // back to spec defaults. The reader doesn't panic.
        let src = "[budgets]\np99_ms = not-a-number\nstddev_ms = 1.04\n";
        let b = parse(src);
        assert_eq!(b.p99_ms, None);
        assert_eq!(b.stddev_ms, Some(1.04));
    }

    #[test]
    fn parse_canonical_repo_file_round_trips() {
        // The shipped tools/frame-pacing/budgets.toml has both fields.
        let body = include_str!("../../../tools/frame-pacing/budgets.toml");
        let b = parse(body);
        assert_eq!(b.p99_ms, Some(18.3));
        assert_eq!(b.stddev_ms, Some(1.04));
    }
}
